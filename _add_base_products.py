# ── 3. Define new products and inject before products array close ───────────
def P(k, g, icon, name, dose, nuts, kcal, prot, fat, carbs, fiber, sugar, sodium,
      desc, use, tags, gpu=None, warn=None):
    """Return a JS product object string."""
    tags_js = "[" + ",".join(f"'{t}'" for t in tags) + "]"
    warn_js = f"'{warn}'" if warn else "null"
    gpu_js = f",gramsPerUnit:{gpu}" if gpu else ""
    return (
        f"  {{k:'{k}',g:'{g}',icon:'{icon}',name:'{name}',dose:'{dose}',"
        f"nuts:{{{nuts}}},"
        f"kcal:{kcal},protein:{prot},fat:{fat},carbsNum:{carbs},"
        f"fiber:{fiber},sugar:{sugar},sodium:{sodium}{gpu_js},"
        f"desc:'{desc}',use:'{use}',useTags:{tags_js},"
        f"season:'Круглый год.',seasonM:[],seasonType:'plant',warn:{warn_js}}},\n"
    )

# Compact nut-flag spec: F=2, P=1, N=0 (matching constants in index.html)
def N(spec):
    """Compose a nuts:{...} body from a 20-tuple spec.
    Order: vitC,vitK,vitA,vitE,bvit,b12,vitD,iron,calcium,mag,iod,zinc,sel,omega,protein,fiber,antioxid,adapt,phyto,carbs
    """
    keys = ['vitC','vitK','vitA','vitE','bvit','b12','vitD','iron','calcium','mag',
            'iod','zinc','sel','omega','protein','fiber','antioxid','adapt','phyto','carbs']
    assert len(spec) == 20
    return ",".join(f"{k}:{v}" for k, v in zip(keys, spec))

